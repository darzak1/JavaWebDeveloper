public class DomCwiczenia {

    public static void main(String[] args) {
        int i = 0;

        do {
            System.out.println("i=" + i);
            i++;
        } while (i <= 100);

        double d = 3.1424234;

        do {
            System.out.println("double= " + d);
            d--;
        } while (d >= -14);

    }
}
